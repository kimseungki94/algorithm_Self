

public class Apt {
	private String money;
	private int aptyear;
	private int year;
	private String dong;
	private String apt;
	private String month;
	private String day;
	private String area;
	private String address;
	private int area_code;
	private int floor;
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public int getAptyear() {
		return aptyear;
	}
	public void setAptyear(int aptyear) {
		this.aptyear = aptyear;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public String getDong() {
		return dong;
	}
	public void setDong(String dong) {
		this.dong = dong;
	}
	public String getApt() {
		return apt;
	}
	public void setApt(String apt) {
		this.apt = apt;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getArea_code() {
		return area_code;
	}
	public void setArea_code(int area_code) {
		this.area_code = area_code;
	}
	public int getFloor() {
		return floor;
	}
	public void setFloor(int floor) {
		this.floor = floor;
	}
	@Override
	public String toString() {
		return "Apt [money=" + money + ", aptyear=" + aptyear + ", year=" + year + ", dong=" + dong + ", apt=" + apt
				+ ", month=" + month + ", day=" + day + ", area=" + area + ", address=" + address + ", area_code="
				+ area_code + ", floor=" + floor + "]";
	}
	
	
	
	
}
